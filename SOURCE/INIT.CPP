/*- Resets lists parameters to the current working directory. -*/
void resetlist(filelist *l)
{
	l->drive=getdisk();
	getcwd(l->path,MAXPATH);
	l->farcomp=0;
}

// important function: updates the filelist. reread the files.
// on far computer reads the directory. on local computer change to the spec directory and then
// rescan the files. on error reads local working directory.
int updatelist(filelist *l)
{
	int i;
	if (l->farcomp) {
		if (!((fargetcwd(l->path,MAXPATH)==0)&&(fargetdisk(&(l->drive))==0)&&(farscanfiles(l->list,MAX_FILES_IN_DIR,"*.*",FA_DIREC,&i)==0))) {
			resetlist(l);
			i = scanfiles(l->list,MAX_FILES_IN_DIR,"*.*",FA_DIREC);
			l->num = i;
			l->sort();
			l->zerotag();
			l->home();
			return 1;
		}
	} else {
		setdisk(l->drive);
		chdir(l->path);
		i = scanfiles(l->list,MAX_FILES_IN_DIR,"*.*",FA_DIREC);
	}

	l->num = i;
	l->sort();
	l->zerotag();
	l->home();
	return 0;
}

//initializes memory and screen (called once at the beggining.
void init()
{
	char buttons[1][BUTTONSIZE] = {
		"OK"};
	int i;


	_setcursortype(_NOCURSOR); // Sets cursor invisable
	showinitscreen();
	directvideo=1;	// For faster graphic preformance.
			// Allocating memory for left window.

	textwin *w;
	// loads the config.
	if ((i=loadconfig())!=0) {
		w = new textwin(15,7,50,3,0x4e,0x4f,"Configuration");
		w->show(1);
		if (i==4) {
			cprintf("Failed loading configuration file (benlink.cfg).\n\r");
			cprintf("Please refer to the manual.\n\r");

		} else {
			cprintf("Invalid configuration file (benlink.cfg).\n\r");
			cprintf("Please refer to the manual.\n\r");
		}
		cprintf("                     ");
		choosebutton(1,buttons,0x4e,0x5f);
		delete w;
	}

	//main left window.
	win1 = new textwin(1,2,FILE_WINS_WIDTH+2,FILE_WINS_LINES,0x1e,0x13,"Local");
	if (!win1) {
		msgbox("Error","Insufficient memory");
		return;
	}
	// Allocating memory for right window.
	win2 = new textwin(41,2,FILE_WINS_WIDTH+2,FILE_WINS_LINES,0x1e,0x13,"Local");
	if (!win2) {
		msgbox("Error","Insufficient memory");
		return;
	}
	// Allocating memory for left filelist.
	lis1 = new filelist(0,MAX_FILES_IN_DIR,0,0x17,0x46,FILE_WINS_LINES-FROMUP-FROMDOWN,3);
	if (!lis1) {
		msgbox("Error","Insufficient memory");
		return;
	}
	// Allocating memory for right filelist.
	lis2 = new filelist(0,MAX_FILES_IN_DIR,0,0x17,0x46,FILE_WINS_LINES-FROMUP-FROMDOWN,3);
	if (!lis2) {
		msgbox("Error","Insufficient memory");
		return;
	}

	print_hot_keys(0x3e,0x35); // Prints hotkeys line.
	print_menu_line(0x3e,0x2f,0);     // and pulldown menus.

	// Reading left file list from drive.
	resetlist(lis1);	// set local computer
	updatelist(lis1);	// update list in current path.

	// Reading right file list from drive.
	resetlist(lis2);
	updatelist(lis2);             //

	// Showing right window first.
	win2->headline=lis2->path;    // headline as right filelist's path.
	win2->show(0);		      // show unlighted.
	lis2->show();		      // show list in window.
	lis2->removecursor();	      // remove cursor from list (not active window).

	win1->headline=lis1->path;
	win1->show(1);                // show left window in lighted colors.
	lis1->show();		      // show left filelist.

	lisp=lis1;                    // points to active filelist.
	winp=win1;                    // points to active window.

}

void shutdown()
{

	_setcursortype(_NORMALCURSOR); //
	delete lis1;
	delete lis2;
	delete win1;
	delete win2;
	clear_hot_keys();      	       // returns screen to previous mode.
	gotoxy(1,1);
	delline();		// Clears the menu line.
	gotoxy(1,24);
	textattr(0x07);       	       //
}


// redraws windows.
void refresh()
{
	if (lisp==lis1) {
		win2->show(0);
		lis2->show();
		lis2->removecursor();
		win1->show(1);
		lis1->show();
	} else {
		win1->show(0);
		lis1->show();
		lis1->removecursor();
		win2->show(1);
		lis2->show();
	}
}

