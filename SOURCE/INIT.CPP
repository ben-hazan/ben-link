// prints the top line.
void print_menu_line(int attr, int boldattr, int cursor)
{
	const char menuline[NUMOFMENUS][MENUSIZE] = {
		"File",
		"Tag",
		"Link",
		"Sort",
		"Configuration",
		"Help"
	};

	int i;
	char tattr;
	struct text_info before;

	gettextinfo(&before);

	window(1,1,80,25);
	gotoxy(1,1);

	for (i = 0 ; i < NUMOFMENUS-1 ; i++) {
		if (cursor==i+1) textattr(boldattr);
		else		 textattr(attr);
		cprintf(" %s ",menuline[i]);
	}

	tattr = attr&0xf0;
	tattr = tattr|0x8;
	textattr(tattr);
	cprintf("BenLink v1.0 coded by Ben Hazan JB/6");

	for (i = wherex() ; i <= 80 ; i++)cprintf(" ");
	gotoxy(80-strlen(menuline[NUMOFMENUS-1]),1);
	if (cursor==NUMOFMENUS) textattr(boldattr);
	else textattr(attr);
	cprintf("%s",menuline[NUMOFMENUS-1]);

	window(before.winleft,before.wintop,before.winright,before.winbottom);
	textattr(before.attribute);
	gotoxy(before.curx,before.cury);
}

